package boxing.UnBoxing.Example;

public class AutoBox {
    public static void main(String[] args) {
        Integer iOb = 100; // int տիպի արժեքի ավտոմատ փաթեթավորում
        int i = iOb; // int տիպի արժեքի ավտոմատ բացում
        System.out.println(i + " " + iOb); // դուրս բերվում 100 և 100 արժեքները
    }
}

