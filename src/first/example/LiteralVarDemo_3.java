package first.example;

public class LiteralVarDemo_3 {
    public static void main(String[] args) {
        // սովորական
        double a = 4.2; // կամ 4.2d
        float d = 23.56f; // կամ 23.56F
        // էքսպոնեցիալ
        double df = 1.2e2; // կամ 1.2e+2
        double gh = 2.3e-2; // կամ 2.3E-2
    }
}

