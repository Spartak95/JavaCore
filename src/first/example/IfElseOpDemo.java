package first.example;

public class IfElseOpDemo {
    public static void main(String[] args) {
        int num_1 = 6;
        int num_2 = 4;
        if (num_1 > num_2){
            System.out.println("Առաջին թիվը մեծ է երկրորդ թվից");
        } else {
            System.out.println("Երկրորդ թիվը մեծ է առաջին թվից");
        }
    }
}

