package first.example;

public class LiteralVarDemo {
    public static void main(String[] args) {
        int a = 0b0010; // 2-ական համակարգ, իսկ 10-ական համակարգով կլինի 2
        int b = 0125; // 8-ական համակարգ, իսկ 10-ական համակարգով կլինի 85
        int c = 0xad20; // 16-ական համակարգ, իսկ 10-ական համակարգով կլինի 44320
    }
}

