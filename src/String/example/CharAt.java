package String.example;

public class CharAt {
    public static void main(String[] args) {
        String s = "Java";
        char c = s.charAt(0);
        System.out.println(c);
        char c2 = s.charAt(1);
        System.out.println(c2);
        char c3 = s.charAt(2);
        System.out.println(c3);
        char c4 = s.charAt(3);
        System.out.println(c4);
    }
}

