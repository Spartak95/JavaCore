package String.example;

public class ContainsDemo {
    public static void main(String[] args) {
        String str = "Դու սիրում ես դիտել Գահերի խաղը";
        System.out.println(str.contains("սիրում"));
        System.out.println(str.contains("Սիրում"));
        System.out.println(str.contains("Գահերի"));
    }
}

